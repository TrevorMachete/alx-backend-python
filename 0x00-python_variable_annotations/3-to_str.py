#!/usr/bin/env python3
"""
This module contains a function `to_str` that converts a float to a string.
"""


def to_str(n: float) -> str:
    """
    This function converts a float to a string.

    Args:
        n (float): The float number.

    Returns:
        str: The string representation of the float.
    """
    return str(n)


if __name__ == "__main__":
    print(to_str(3.14))
